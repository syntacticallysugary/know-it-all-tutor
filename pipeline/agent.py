# ── Slug / stem derivation ────────────────────────────────────────────────────
_ABBREVS = {
    "management": "mgmt",
    "preparation": "prep",
    "architectural": "arch",
    "infrastructure": "infra",
    "introduction": "intro",
    "operations": "ops",
}


def derive_slug(domain_name: str) -> str:
    """'Java — String Operations' → 'string_ops'"""
    label = domain_name.split(" — ", 1)[-1]
    slug = label.lower()
    slug = slug.replace("/", "")  # "I/O" → "io"
    slug = re.sub(r"[\s\-]+", "_", slug)  # spaces/hyphens → _
    slug = re.sub(r"[^\w]", "", slug)  # strip remaining non-word chars
    slug = re.sub(r"_+", "_", slug).strip("_")

    for long, short in _ABBREVS.items():
        slug = slug.replace(long, short)

    for suffix in ("_foundations", "_overview"):
        if slug.endswith(suffix):
            slug = slug[: -len(suffix)]

    # "x_and_y": replace connector, then truncate if still long
    slug = re.sub(r"_and_", "_", slug)
    if len(slug) > 15:
        slug = slug.split("_")[0]

    return slug


def derive_stem(upload_path: Path) -> tuple[str, str, Path]:
    """Returns (stem, stem_nodash, fix_output_path).
    'java_upload.json'        → ('java', 'java', .../java_upload-fix.json)
    'java_upload-fix.json'    → ('java', 'java', .../java_upload-fix.json)
    'ai_ml_upload-fix.json'   → ('ai_ml', 'aiml', .../ai_ml_upload-fix.json)
    The fix output path is always the -fix.json variant, never the raw source.
    """
    stem = re.sub(r"_upload.*$", "", upload_path.name)
    stem_nodash = stem.replace("_", "")
    fix_path = upload_path.parent / f"{stem}_upload-fix.json"
    return stem, stem_nodash, fix_path


# ── Script generation ─────────────────────────────────────────────────────────
def _write_split_script(
    domains: list[tuple[int, str, str, int]],
    stem_nodash: str,
    upload_filename: str,
    output_dir: Path,
) -> Path:
    path = output_dir / f"split_{stem_nodash}_domains.py"
    if path.exists():
        return path

    slugs_block = "\n".join(f'    "{slug}",' for _, slug, _, _ in domains)
    n = len(domains)
    path.write_text(f'''\
#!/usr/bin/env python3
"""
Split {upload_filename} into {n} per-sub-domain files.
Generated by pipeline/run_pipeline.py — do not edit manually.
"""
import json, os

SLUGS = [
{slugs_block}
]

def main():
    data_dir = os.path.dirname(os.path.abspath(__file__))
    src = os.path.join(data_dir, "..", "{upload_filename}")
    with open(src) as f:
        data = json.load(f)
    domains = data["domains"]
    assert len(domains) == len(SLUGS), f"Expected {{len(SLUGS)}} domains, got {{len(domains)}}"
    for i, (domain, slug) in enumerate(zip(domains, SLUGS)):
        out_path = os.path.join(data_dir, f"domain_{{i:02d}}_{{slug}}.json")
        payload = {{
            "domain_index": i,
            "domain_name": domain["data"]["name"],
            "term_count": len(domain["terms"]),
            "terms": domain["terms"],
        }}
        with open(out_path, "w") as f:
            json.dump(payload, f, indent=2)
        print(f"  {{i:02d}}. {{domain[\'data\'][\'name\']:40s}} → {{os.path.basename(out_path)}}  ({{len(domain[\'terms\'])}} terms)")
    print(f"\\nCreated {{len(domains)}} domain files in {{data_dir}}/")

if __name__ == "__main__":
    main()
''')
    return path


def _write_merge_script(
    domains: list[tuple[int, str, str, int]],
    stem_nodash: str,
    src_filename: str,
    fix_filename: str,
    display_prefix: str,
    output_dir: Path,
) -> Path:
    path = output_dir / f"merge_{stem_nodash}_domains.py"
    if path.exists():
        return path

    n = len(domains)
    domains_block = "\n".join(f'    ({idx}, "{slug}", "{name}"),' for idx, slug, name, _ in domains)
    path.write_text(f'''\
#!/usr/bin/env python3
"""
Checkpoint reporter and merger for {display_prefix} domain definition cleanup.
Generated by pipeline/run_pipeline.py — do not edit manually.

Usage:
  python3 merge_{stem_nodash}_domains.py           # report status
  python3 merge_{stem_nodash}_domains.py --merge   # merge all {n} completed domains
"""
import json, os, sys

DOMAINS = [
{domains_block}
]

def main():
    do_merge = "--merge" in sys.argv
    data_dir = os.path.dirname(os.path.abspath(__file__))
    src_path = os.path.join(data_dir, "..", "{src_filename}")
    out_path = os.path.join(data_dir, "..", "{fix_filename}")

    with open(src_path) as f:
        source = json.load(f)

    print("=== {display_prefix} Domain Cleanup Checkpoint ===\\n")
    completed, errors, pending = [], [], []

    for idx, slug, display_name in DOMAINS:
        fix_path = os.path.join(data_dir, f"domain_{{idx:02d}}_{{slug}}_fix.json")
        if os.path.exists(fix_path):
            try:
                with open(fix_path) as f:
                    fix_data = json.load(f)
                terms = fix_data.get("terms", [])
                expected = len(source["domains"][idx]["terms"])
                if len(terms) != expected:
                    raise ValueError(f"expected {{expected}} terms, got {{len(terms)}}")
                print(f"  ✓  {{idx:02d}}. {{display_name:40s}} ({{len(terms)}} terms)")
                completed.append((idx, slug, fix_path, fix_data))
            except (json.JSONDecodeError, ValueError, KeyError) as e:
                print(f"  ✗  {{idx:02d}}. {{display_name:40s}} (ERROR: {{e}})")
                errors.append((idx, slug, str(e)))
        else:
            print(f"  …  {{idx:02d}}. {{display_name:40s}} (pending)")
            pending.append((idx, slug))

    total = len(DOMAINS)
    print(f"\\n{{len(completed)}}/{{total}} done  |  {{len(errors)}} errors  |  {{len(pending)}} pending")

    if pending:
        next_idx, next_slug = pending[0]
        print(f"\\nNext: domain_{{next_idx:02d}}_{{next_slug}}.md")
    if errors:
        print("\\nFix errors before merging:")
        for idx, slug, msg in errors:
            print(f"  domain_{{idx:02d}}_{{slug}}_fix.json — {{msg}}")

    if not do_merge:
        if len(completed) == total and not errors:
            print(f"\\nAll {{total}} domains complete. Run with --merge to update {fix_filename}")
        return

    if errors or pending:
        print("\\nCannot merge: not all domains are complete and error-free.")
        sys.exit(1)

    print(f"\\nMerging all {{total}} domains into {fix_filename} ...")
    merged = {{"domains": []}}
    for idx, slug, fix_path, fix_data in completed:
        original = source["domains"][idx]
        merged["domains"].append({{
            "node_type": original["node_type"],
            "data":      original["data"],
            "terms":     fix_data["terms"],
        }})

    with open(out_path, "w") as f:
        json.dump(merged, f, indent=2)

    total_terms = sum(len(d["terms"]) for d in merged["domains"])
    print(f"Written: {{out_path}}  ({{total_terms}} terms across {{len(merged[\'domains\'])}} domains)")

if __name__ == "__main__":
    main()
''')
    return path


