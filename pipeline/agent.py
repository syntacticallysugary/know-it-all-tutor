# ── Tool dispatcher ───────────────────────────────────────────────────────────
def _dispatch(name: str, args: dict) -> str:
    if name == "read_file":
        p = Path(args["path"])
        if not p.exists():
            return f"ERROR: file not found: {args['path']}"
        return p.read_text()
    if name == "write_file":
        p = Path(args["path"])
        p.parent.mkdir(parents=True, exist_ok=True)
        p.write_text(args["content"])
        return "OK"
    return f"ERROR: unknown tool '{name}'"


def _sparky(messages: list, tools: list) -> dict:
    """Stream from Sparky and accumulate into a single response dict.
    Streaming avoids read timeouts on long reasoning chains.
    max_tokens=8192 is sufficient for cleanup (short_reference generation needs 32768).
    """
    payload = {
        "model": SPARKY_MODEL,
        "messages": messages,
        "tools": tools,
        "tool_choice": "auto",
        "max_tokens": 24576,
        "temperature": 0.2,
        "stream": True,
    }

    content_parts: list[str] = []
    reasoning_parts: list[str] = []
    tool_calls_acc: dict[int, dict] = {}
    finish_reason: str | None = None

    with requests.post(
        f"{SPARKY_BASE}/chat/completions",
        json=payload,
        stream=True,
        timeout=(30, None),  # 30s connect, unlimited read
    ) as resp:
        resp.raise_for_status()
        for raw in resp.iter_lines():
            if not raw or not raw.startswith(b"data: "):
                continue
            payload_bytes = raw[6:]
            if payload_bytes == b"[DONE]":
                break
            chunk = json.loads(payload_bytes)
            choice = chunk["choices"][0]
            finish_reason = choice.get("finish_reason") or finish_reason
            delta = choice.get("delta", {})

            if delta.get("content"):
                content_parts.append(delta["content"])
            if delta.get("reasoning"):
                reasoning_parts.append(delta["reasoning"])

            for tc in delta.get("tool_calls") or []:
                i = tc["index"]
                if i not in tool_calls_acc:
                    tool_calls_acc[i] = {
                        "id": "",
                        "type": "function",
                        "function": {"name": "", "arguments": ""},
                    }
                if tc.get("id"):
                    tool_calls_acc[i]["id"] = tc["id"]
                fn = tc.get("function", {})
                if fn.get("name"):
                    tool_calls_acc[i]["function"]["name"] += fn["name"]
                if fn.get("arguments"):
                    tool_calls_acc[i]["function"]["arguments"] += fn["arguments"]

    return {
        "choices": [
            {
                "finish_reason": finish_reason,
                "message": {
                    "role": "assistant",
                    "content": "".join(content_parts) or None,
                    "reasoning": "".join(reasoning_parts) or None,
                    "tool_calls": list(tool_calls_acc.values()),
                },
            }
        ]
    }


# ── Per-domain cleanup agent ──────────────────────────────────────────────────
def run_cleanup_agent(
    prompt_path: Path,
    fix_path: Path,
    term_count: int,
    log,
    max_turns: int = 25,
) -> bool:
    """Run a single-domain cleanup agent against Sparky.
    Reads the prompt from prompt_path (.md file).
    log: open file object — turn-level details written here, not to stdout.
    Returns True if _fix.json was successfully written and validated.
    Skips without calling Sparky if fix_path already exists.
    """
    if fix_path.exists():
        log.write(f"[{fix_path.name}] skipped — already done\n")
        print("(already done)")
        return True

    prompt = prompt_path.read_text()

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": prompt},
    ]

    wrote_fix = False
    for turn in range(max_turns):
        data = _sparky(messages, TOOLS)
        choice = data["choices"][0]
        msg = choice["message"]
        messages.append(msg)

        finish = choice.get("finish_reason", "stop")
        tool_calls = msg.get("tool_calls") or []

        if finish == "tool_calls" or tool_calls:
            tool_results = []
            for tc in tool_calls:
                fn_name = tc["function"]["name"]
                try:
                    args = json.loads(tc["function"]["arguments"])
                except json.JSONDecodeError:
                    log.write(f"  turn {turn + 1}: FAIL — tool call arguments truncated\n")
                    log.flush()
                    print("\n     FAIL: see log for details")
                    return False
                path_arg = str(args.get("path", ""))
                log.write(f"  turn {turn + 1}: {fn_name}({path_arg})\n")
                log.flush()
                result = _dispatch(fn_name, args)
                tool_results.append(
                    {
                        "role": "tool",
                        "tool_call_id": tc["id"],
                        "content": result,
                    }
                )
                if fn_name == "write_file":
                    wrote_fix = True
            messages.extend(tool_results)
            if wrote_fix:
                break
        else:
            # finish_reason == "stop"
            content_len = len(msg.get("content") or "")
            log.write(
                f"  turn {turn + 1}: stop — content_len={content_len}, wrote_fix={wrote_fix}\n"
            )
            log.flush()
            if wrote_fix:
                break
            # Model output text instead of calling write_file — redirect it
            messages.append(
                {
                    "role": "user",
                    "content": (
                        "You must save your output by calling write_file — do not output JSON as text. "
                        f"Call write_file now with path='{fix_path}' "
                        "and the complete rewritten JSON as the content argument."
                    ),
                }
            )

    if not wrote_fix:
        msg = f"FAIL: {max_turns} turns completed without write_file call"
        log.write(f"  {msg}\n")
        log.flush()
        print(f"\n     {msg}")
        return False
    if not fix_path.exists():
        msg = f"FAIL: write_file called but {fix_path.name} not found on disk"
        log.write(f"  {msg}\n")
        log.flush()
        print(f"\n     {msg}")
        return False

    # Validate: parseable JSON with correct term count
    try:
        with open(fix_path) as f:
            data = json.load(f)
        actual = len(data.get("terms", []))
        if actual != term_count:
            msg = f"FAIL: expected {term_count} terms, got {actual}"
            log.write(f"  {msg}\n")
            log.flush()
            print(f"\n     {msg}")
            return False
    except json.JSONDecodeError as e:
        msg = f"FAIL: output is not valid JSON: {e}"
        log.write(f"  {msg}\n")
        log.flush()
        print(f"\n     {msg}")
        return False

    return True


# ── Main pipeline ─────────────────────────────────────────────────────────────
def run_pipeline(upload_path: Path) -> None:
    upload_path = upload_path.resolve()
    if not upload_path.exists():
        sys.exit(f"Error: {upload_path} not found")

    stem, stem_nodash, fix_output_path = derive_stem(upload_path)
    output_dir = upload_path.parent / f"{stem}_domains"
    output_dir.mkdir(exist_ok=True)

    with open(upload_path) as f:
        data = json.load(f)

    raw_domains = data["domains"]
    display_prefix = raw_domains[0]["data"]["name"].split(" — ")[0] if raw_domains else stem.upper()

    domains = [
        (i, derive_slug(d["data"]["name"]), d["data"]["name"], len(d["terms"]))
        for i, d in enumerate(raw_domains)
    ]

    data_dir_rel = f"data/{stem}_domains"
    prompt_dir = Path(__file__).parent / "02_cleanup" / stem

    # ── Step 1: Generate split, merge, and generate_prompts scripts ───────────
    print(f"\n[1/4] Preparing {output_dir.name}/")
    for idx, slug, name, count in domains:
        print(f"       {idx:02d}. {name:40s} → slug: {slug}  ({count} terms)")

    split_script = _write_split_script(domains, stem_nodash, upload_path.name, output_dir)
    merge_script = _write_merge_script(
        domains,
        stem_nodash,
        src_filename=upload_path.name,
        fix_filename=fix_output_path.name,
        display_prefix=display_prefix,
        output_dir=output_dir,
    )
    gen_py = _write_generate_prompts_script(domains, stem, data_dir_rel, prompt_dir)
    print(f"  scripts: {split_script.name}, {merge_script.name}, {gen_py.name}")

    # ── Step 2: Split into per-domain JSON files ──────────────────────────────
    first = output_dir / f"domain_00_{domains[0][1]}.json"
    if not first.exists():
        print(f"\n[2/4] Splitting {upload_path.name} ...")
        subprocess.run([sys.executable, str(split_script)], check=True)
    else:
        print("\n[2/4] Per-domain files already present, skipping split.")

    # ── Step 3: Generate per-domain cleanup prompt .md files ─────────────────
    first_md = prompt_dir / f"domain_00_{domains[0][1]}.md"
    if not first_md.exists():
        print(f"\n[3/4] Generating cleanup prompts in {prompt_dir.relative_to(PROJECT_ROOT)}/")
        subprocess.run([sys.executable, str(gen_py)], check=True)
    else:
        print("\n[3/4] Cleanup prompts already present, skipping generate.")

    # ── Step 4: Per-domain cleanup via Sparky ────────────────────────────────
    log_dir = PROJECT_ROOT / "pipeline" / "logs"
    log_dir.mkdir(exist_ok=True)
    from datetime import datetime

    log_path = log_dir / f"{stem}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.log"

    print(f"\n[4/4] Cleanup — {len(domains)} domains via Sparky")
    print(f"      log: {log_path.relative_to(PROJECT_ROOT)}")
    failed = []

    with open(log_path, "w") as log:
        log.write(f"Pipeline run: {upload_path.name}\n")
        log.write(f"Domains: {len(domains)}\n\n")

        for idx, slug, display_name, term_count in domains:
            prompt_path = prompt_dir / f"domain_{idx:02d}_{slug}.md"
            fix_path = output_dir / f"domain_{idx:02d}_{slug}_fix.json"
            print(f"  {idx:02d}. {display_name} ({term_count} terms) ... ", end="", flush=True)
            log.write(f"[{idx:02d}] {display_name} ({term_count} terms)\n")
            log.flush()
            ok = run_cleanup_agent(prompt_path, fix_path, term_count, log)
            status = "ok" if ok else "FAILED"
            log.write(f"  result: {status}\n\n")
            log.flush()
            print("✓" if ok else "✗ FAILED")
            if not ok:
                failed.append((idx, slug))

    if failed:
        names = ", ".join(f"domain_{i:02d}_{s}" for i, s in failed)
        print(f"\n  {len(failed)} domain(s) failed: {names}")
        print("  Re-run to retry failed domains (completed ones will be skipped).")
        sys.exit(1)

    # ── Step 5: Merge ─────────────────────────────────────────────────────────
    print(f"\n[5/5] Merge → {fix_output_path.name}")
    subprocess.run([sys.executable, str(merge_script), "--merge"], check=True)
    print("\nPipeline complete.")
